# Function to add the sum of two arguments
def add(x, y):
    z = x + y
    return z

# Function to subtract the second argument by the first
def sub(x, y):
    z = x - y
    return z

# Function to multiply two arguments
def mult(x, y):
    z = x * y
    return z

# Function to divide the first argument by the second
def div(x, y):
    z = x / y
    return z

# Example Numbers
num0 = 22
num1 = 2

print(add(num0, num1))
print(sub(num0, num1))
print(mult(num0, num1))
print(div(num0, num1))